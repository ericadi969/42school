int			ft_sqrt(int nb)
{
	int r;
	int s;

	r = 1;
	s = 0;
	while (r <= nb / 2)
	{
		s = r * r;
		if (s == nb)
			return (r);
		r = r + 1;
	}
	return (0);
}
